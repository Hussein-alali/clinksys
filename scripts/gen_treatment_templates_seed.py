"""
Generator for seed-treatment-templates.sql

Authors real, diagnosis-specific physiotherapy treatment-plan templates and
emits an IDEMPOTENT Supabase seed that:
  • creates the two categories (قوالب التشخيص للعمود الفقري / قوالب تشخيص الكتف)
    only when a category with that name doesn't already exist;
  • inserts one template per diagnosis into treatment_templates with a
    STABLE template_id, using `on conflict (template_id) do nothing`, so
    re-running never duplicates.

The frontend stores/reads `treatment_templates.category` as the category
NAME (free text) — the template_categories table is the authoritative
picker list — so each template's `category` is set to the exact category
name, linking it to its category.

JSONB shapes match what the app renders:
  goals     : ["..."]                       (short- + long-term goals)
  exercises : [{name,sets,reps,duration,hold_time,rest_time,equipment,
               description,notes}]           (therapeutic/stretch/strength)
  methods   : [{name}]                       (manual therapy + techniques)

Extra clinical fields the schema has no dedicated column for (clinical
summary, symptoms, examination findings, clinical notes, precautions,
contraindications) are consolidated with clear Arabic headings into the
`notes` and `warnings` columns.

Run:  python3 scripts/gen_treatment_templates_seed.py > seed-treatment-templates.sql
"""

