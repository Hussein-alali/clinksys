# ── SQL assembly ──────────────────────────────────────────────────────────
def sql_str(s):
    """SQL single-quoted literal with single-quote escaping (Arabic safe)."""
    return "'" + s.replace("'", "''") + "'"

def sql_json(obj):
    """JSON literal cast to jsonb, single quotes escaped."""
    return "'" + json.dumps(obj, ensure_ascii=False).replace("'", "''") + "'::jsonb"

def build_notes(d):
    return (
        "الملخص السريري: " + d["summary"] + "\n\n" +
        "الأعراض الشائعة: " + d["symptoms"] + "\n\n" +
        "نتائج الفحص: " + d["exam"] + "\n\n" +
        "ملاحظات سريرية: " + d.get("notes", "")
    ).strip()

def build_warnings(d):
    return (
        "الاحتياطات: " + d["precautions"] + "\n\n" +
        "موانع الاستعمال: " + d["contra"]
    ).strip()

def build_goals(d):
    goals = []
    for g in d["st"]:
        goals.append("قصير المدى — " + g)
    for g in d["lt"]:
        goals.append("طويل المدى — " + g)
    return goals

def build_followup(d):
    return ("التكرار الموصى به: {freq} جلسات أسبوعيًا. "
            "إعادة التقييم كل 4–6 جلسات وتعديل الخطة وفق الاستجابة."
            ).format(freq=d["freq"])

def emit_templates(cat_name, body_part, prefix, items):
    out = []
    idx = 0
    for dx, d in items.items():
        idx += 1
        tid = "TPL-SEED-%s-%02d" % (prefix, idx)
        name = dx + " — خطة علاج طبيعي"
        goals = build_goals(d)
        methods = [{"name": m} for m in d["methods"]]
        exercises = d["ex"]
        notes = build_notes(d)
        warnings = build_warnings(d)
        followup = build_followup(d)
        home = d["home"]
        out.append(
f"""insert into treatment_templates
  (template_id, name, category, diagnosis, body_part, goals, exercises, methods,
   home_instructions, notes, warnings, followup_instructions,
   estimated_sessions, weekly_frequency, expected_recovery_days,
   status, created_by_name)
values
  ({sql_str(tid)}, {sql_str(name)}, {sql_str(cat_name)}, {sql_str(dx)}, {sql_str(body_part)},
   {sql_json(goals)}, {sql_json(exercises)}, {sql_json(methods)},
   {sql_str(home)}, {sql_str(notes)}, {sql_str(warnings)}, {sql_str(followup)},
   {d['sessions']}, {d['freq']}, {d['recovery']},
   'active', 'بذرة النظام')
on conflict (template_id) do nothing;"""
        )
    return out


def main():
    lines = []
    lines.append("-- ═══════════════════════════════════════════════════════════════")
    lines.append("-- SEED: treatment-plan template categories + diagnosis templates")
    lines.append("-- GENERATED by scripts/gen_treatment_templates_seed.py — DO NOT EDIT")
    lines.append("-- BY HAND. Re-run the generator to regenerate.")
    lines.append("--")
    lines.append("-- Idempotent: categories insert only when a same-named category is")
    lines.append("-- absent; every template uses a stable template_id with")
    lines.append("-- `on conflict (template_id) do nothing`, so running this multiple")
    lines.append("-- times never creates duplicates. Templates link to their category")
    lines.append("-- by name (treatment_templates.category = template_categories.name),")
    lines.append("-- which is how the template picker in the app resolves them.")
    lines.append("-- ═══════════════════════════════════════════════════════════════")
    lines.append("")
    lines.append("begin;")
    lines.append("")
    lines.append("-- ── Categories (idempotent by name) ──────────────────────────")
    for cid, cname, order in [
        ("TPC-SEED-SPINE", SPINE_CAT, 10),
        ("TPC-SEED-SHOULDER", SHOULDER_CAT, 20),
    ]:
        lines.append(
f"""insert into template_categories (category_id, name, description, status, sort_order, created_by_name)
select {sql_str(cid)}, {sql_str(cname)}, {sql_str('فئة تشخيصية للعلاج الطبيعي')}, 'active', {order}, 'بذرة النظام'
where not exists (
  select 1 from template_categories where lower(trim(name)) = lower(trim({sql_str(cname)}))
);"""
        )
    lines.append("")
    lines.append("-- ── Spine templates (%d) ──────────────────────────────────────" % len(SPINE))
    lines += emit_templates(SPINE_CAT, "العمود الفقري", "SPN", SPINE)
    lines.append("")
    lines.append("-- ── Shoulder templates (%d) ───────────────────────────────────" % len(SHOULDER))
    lines += emit_templates(SHOULDER_CAT, "الكتف", "SHD", SHOULDER)
    lines.append("")
    lines.append("commit;")
    lines.append("")
    lines.append("-- Refresh PostgREST so the seeded rows are immediately queryable.")
    lines.append("notify pgrst, 'reload schema';")
    lines.append("")
    print("\n".join(lines))


if __name__ == "__main__":
    main()
